import { useEffect, useState } from "react";
import reactLogo from "./assets/react.svg";
import viteLogo from "/vite.svg";
import "./App.css";
import axios from "axios"; //axios is use to  fetch backend data

function App() {
  const [jokes, setjokes] = useState([]);

  useEffect(() => {
    axios.get("/api/jokes") //fetch this api  ,i can created before
      .then((response) => {
        setjokes(response.data);
      })
      .catch((error) => {
        console.log(error);
      });
  });

  return (
    <>
      <h1>heloo pratap</h1>
      <p>jokes:{jokes.length}</p>

      {jokes.map((joke, index) => {
        return (
          <div key={joke.id}>
            <h3>{joke.title}</h3>
            <p>{joke.content}</p>
          </div>
        );
      })}
    </>
  );
}

export default App;
